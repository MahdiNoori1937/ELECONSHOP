using System.Text.RegularExpressions;

namespace ELECON.Application.Extensions;

public static class EmialRegex
{
    private static readonly string EmailRegexCode =
        "(?:[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*|\"" +
        "(?:[\\x01-\\x08\\x0b\\x0c\\x0e-\\x1f\\x21\\x23-\\x5b\\x5d-\\x7f]|\\\\[\\x01-\\x09\\x0b\\x0c\\x0e-\\x7f])*\")@(?:(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?|\\" +
        "[(?:(?:(2(5[0-5]|[0-4][0-9])|1[0-9][0-9]|[1-9]?[0-9]))\\.){3}(?:(2(5[0-5]|[" +
        "0-4][0-9])|1[0-9][0-9]|[1-9]?[0-9])|[a-z0-9-]*[a-z0-9]:(?:[\\x01-\\x08\\x0b\\x" +
        "0c\\x0e-\\x1f\\x21-\\x5a\\x53-\\x7f]|\\\\[\\x01-\\x09\\x0b\\x0c\\x0e-\\x7f])+)\\])";
    private static readonly string NumberRegexCode = @"^09[0-9]{9}$";
    public static bool EmailIsValid(this string email)
    {
        Regex regex = new Regex(EmailRegexCode);

        return regex.IsMatch(email);
    }

    public static bool NumberValid(this string number)
    {
        Regex regex = new Regex(NumberRegexCode);

        return regex.IsMatch(number);
    }
}